    private void btnConsultarAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarAltActionPerformed
   
        try {
            
            
            String dados = txtNome1.getText();
            pr = p.consultar(dados);
            
             
          txtNome1.setText(pr.getNome());
   
          txtNomeConsulta.setText(pr.getNome());
          String codigo = pr.getCodigo();
          txtCodigo1.setText(codigo);
          String unidmedida = pr.getUnidadeMedida();
          txtUnidadeDeMedida1.setText(unidmedida);
          String qtdestoque = Double.toString(pr.getQtdEstoque());
          txtQtdEmEstoque1.setText(qtdestoque);
          String valorvenda = Double.toString(pr.getValorVenda());
          txtValorVenda1.setText(valorvenda);
          String valorcompra = Double.toString(pr.getValorCompra());
          txtValorCompra1.setText(valorcompra);
          String qtdEstoqueMinimo = Double.toString(pr.getQtdEstoqueMin());
          txtQtdEstoqueMinimo1.setText(qtdEstoqueMinimo);

        } catch (Exception error) {
            System.out.println(error.getMessage());
        }
        
    }//GEN-LAST:event_btnConsultarAltActionPerformed

